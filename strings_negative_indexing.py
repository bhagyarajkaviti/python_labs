string_1 = "program"
string_2 = string_1[-1]
print(string_2)     #Output :m

#Example 2:
string_1 = "program"
string_2 = string_1[-4:-1]
print(string_2)     #Output :gra

#Example 3:
string_1 = "program"
string_2 = string_1[6:0:-2]
print(string_2)     #Output :mro

#Example 4: String Reversing
string_1 = "program"
string_2 = string_1[::-1]
print(string_2)     #Output :margorp